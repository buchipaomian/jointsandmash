def add_a_type_in_mask(orimask,a_point,value):
    mask = orimask.copy()
    y = a_point[0]
    x = a_point[1]
    height,width = mask.shape[:2]
    if mask[y][x] == 0:
        return mask#如果点在区域外面就不处理
    h_length = -1
    h_not_found = True
    while h_not_found:
        h_length += 1
        up = y-h_length
        down = y + h_length
        uppart = 0
        downpart = 0
        if up >= 0:
            uppart = mask[up][x]
        if down < height:
            downpart = mask[down][x]
        if (uppart+downpart) == 0:
            h_not_found = False
        elif up <=0 and down >= (height-1):
            h_not_found = False
    
    w_length = -1
    w_not_found = True
    while w_not_found:
        w_length += 1
        left = x-w_length
        right = x + w_length
        leftpart = 0
        rightpart = 0
        if left >= 0:
            leftpart = mask[y][left]
        if right < width:
            rightpart = mask[y][right]
        if (leftpart+rightpart) == 0:
            w_not_found = False
        elif left <=0 and right >= (width-1):
            w_not_found = False
    #这里从横向与竖向找到两个方向上能够截断区域的距离，找到这两个距离的最小值，就可以画出来一个最小的隔断区域的圆
    r = min(h_length,w_length)
    left_edge = max(0,x-r)
    righr_edge = min(width,x+r)
    up_edge = max(0,y-r)
    down_edge = min(height,y+r)
    for i in range(up_edge,down_edge):
        for j in range(left_edge,righr_edge):
            if (i-y)**2 + (j-x)**2 <= r**2:
                if mask[i][j] != 0:
                    mask[i][j] = value
    return mask

